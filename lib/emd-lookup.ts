/** 읍면동 이름으로 시군구 정보 조회 */
export function lookupEmd(query: string): SggInfo | null {
  const normalized = query.trim()
  // 완전 일치
  if (EMD_MAP[normalized]) return EMD_MAP[normalized]
  // 접미 '동'/'읍'/'면'/'리' 제거 후 재검색
  const stripped = normalized.replace(/(동|읍|면|리)$/, '')
  const found = Object.keys(EMD_MAP).find(k => k.startsWith(stripped) || k.replace(/(동|읍|면|리)$/, '') === stripped)
  return found ? EMD_MAP[found] : null
}
